
public class Runner_Arttirim_eksiltim {
	public static void main(String[] args) {

		// Arttýrým
		int sayi = 5;
		System.out.println("Sayý : " + sayi + 1);

		sayi++;
		System.out.println("Sayý : " + sayi);

		// Eksiltim
		int sayi1 = 5;
		sayi1--;
		System.out.println("Sayý : " + sayi1);

	}

}
